// Returns the age group index based on the input age
int ageGroup(int age) {
    if (age < 22) return 1;
    else if (age < 25) return 2;
    else if (age < 28) return 3;
    else if (age < 31) return 4;
    else if (age < 34) return 5;
    else if (age < 37) return 6;
    else if (age < 40) return 7;
    else if (age < 43) return 8;
    else if (age < 46) return 9;
    else if (age < 49) return 10;
    else if (age < 52) return 11;
    else if (age < 55) return 12;
    else if (age < 58) return 13;
    else return 14;
}

